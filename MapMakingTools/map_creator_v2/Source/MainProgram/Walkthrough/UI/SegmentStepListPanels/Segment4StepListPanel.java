    @Override
    public void UpdateControls()
    {
        WalkthroughStep step = m_walkthroughWindow.GetCurrentStep();
        WalkthroughStep fstep = m_walkthroughWindow.GetFarthestStep();
        v_step1Button.setEnabled(fstep.GetSegment() > 3 || fstep.GetStep() >= -1);
        v_step2Button.setEnabled(fstep.GetSegment() > 3 || fstep.GetStep() >= 0);

        v_step1Button.setBackground(GetConstantColorFromStepIndex(0, fstep.GetSegment(), step.GetStep(), fstep.GetStep()));
        v_step2Button.setBackground(GetConstantColorFromStepIndex(1, fstep.GetSegment(), step.GetStep(), fstep.GetStep()));
    }
    
    @Override
    public void FocusOnFirstButton()
    {
        v_step1Button.requestFocusInWindow();
    }
    
    private Color GetConstantColorFromStepIndex(int buttonIndex, int fsegment, int step, int stepIndex)
    {
        if(fsegment > 3)
        {
            if(step == buttonIndex)
                return Constants.HighlightedSegmentBackColor;
            else
                return Constants.FinishedSegmentBackColor;
        }
        else
        {
            if(step == buttonIndex)
                return Constants.HighlightedSegmentBackColor;
            else if(stepIndex <= buttonIndex)
                return Constants.DefaultButtonBackColor;
            else
                return Constants.FinishedSegmentBackColor;
        }
    }

