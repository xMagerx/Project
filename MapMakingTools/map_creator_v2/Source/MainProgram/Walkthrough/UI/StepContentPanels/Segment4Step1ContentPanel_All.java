    private void v_exportAndLaunchActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_v_exportAndLaunchActionPerformed
    {//GEN-HEADEREND:event_v_exportAndLaunchActionPerformed
        File tripleaJarOrExeFile = null;
        if(v_installedTripleAList.getModel().getSize() == 0) //If TripleA is not installed where we think it is
        {
            tripleaJarOrExeFile = FileFinder.FindTripleAJarOrExeFile(m_walkthroughWindow, null);
        }
        else
        {
            if(v_installedTripleAList.getSelectedValue() == null)
            {
                JOptionPane.showMessageDialog(m_walkthroughWindow, "Please select the version of TripleA you want to test your map in.");
                return;
            }
            
            File selectedTripleAFolder = new File(FolderFinder.getTripleAParentRootFolder(), v_installedTripleAList.getSelectedValue().toString());
            
            tripleaJarOrExeFile = FileFinder.FindTripleAJarOrExeFile(m_walkthroughWindow, selectedTripleAFolder);
        }
        
        //Clean out folder before proceeding
        try
        {
            if (GetMapExportFolder().exists())
                FileSystem.deleteDirectoryAndContents(GetMapExportFolder());
        }
        catch(Exception ex)
        {
        }
        MapDataExporter exporter = new MapDataExporter();
        exporter.ExportMapToFolder(GetMapData(), GetMapExportFolder(), true, m_walkthroughWindow);
        
        Process tripleaProcess = CommandHelper.executeCommandAndGetResultingProcess("java -jar \"" + tripleaJarOrExeFile.getPath() + "\" \"" + GetMapExportGameFile().getPath() + "\"");
        //try{tripleaProcess.waitFor();}catch (InterruptedException ex){}
    }//GEN-LAST:event_v_exportAndLaunchActionPerformed

