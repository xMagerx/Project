    @Override
    public void InitControls()
    {
        DefaultListModel model = new DefaultListModel();
        
        File tripleAParentRootFolder = FolderFinder.getTripleAParentRootFolder();
        if(!tripleAParentRootFolder.exists())
            return; //Yikes
        
        File latestTripleAFolder = null;
        Version latestVersionFound = new Version(0, 0, 0, 0);
        
        for(File tripleaVersionRoot : tripleAParentRootFolder.listFiles())
        {
            if(tripleaVersionRoot.isDirectory())
            {
                try
                {
                    String folderName = tripleaVersionRoot.getName().trim().toLowerCase();
                    String versionSection = folderName.substring(folderName.indexOf("a") + 2);
                    Version version = new Version(versionSection.replace("_", "."));
                    
                    if(version.compareTo(latestVersionFound) > 0)
                    {
                        latestTripleAFolder = tripleaVersionRoot;
                        latestVersionFound = version;
                    }
                    
                    model.addElement(folderName);
                }
                catch(Exception ex)
                {
                }
            }
        }
        
        v_installedTripleAList.setModel(model);
        if(latestTripleAFolder != null)
            v_installedTripleAList.setSelectedValue(latestTripleAFolder.getName(), true);
    }

